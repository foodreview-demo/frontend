// 시/도 목록
export const getRegions = (): string[] => {
  return Object.keys(REGIONS)
}

// 특정 시/도의 구/군 목록
export const getDistricts = (region: string): string[] => {
  return region && REGIONS[region] ? Object.keys(REGIONS[region]) : []
}

// 특정 구/군의 동 목록
export const getNeighborhoods = (region: string, district: string): string[] => {
  return region && district && REGIONS[region]?.[district] ? REGIONS[region][district] : []
}

// 주소에서 지역 정보 파싱 (카카오맵 주소 → region, district, neighborhood)
export const parseAddress = (address: string): { region: string; district: string; neighborhood: string } => {
  const parts = address.split(' ').filter(p => p.trim())

  let region = ''
  let district = ''
  let neighborhood = ''

  if (parts.length >= 1) {
    // 시/도 추출 (서울특별시 → 서울, 경기도 → 경기)
    const r = parts[0]
      .replace('특별시', '')
      .replace('광역시', '')
      .replace('특별자치시', '')
      .replace('특별자치도', '')
      .replace('도', '')
    region = r
  }

  if (parts.length >= 2) {
    district = parts[1]
  }

  if (parts.length >= 3) {
    // 동/읍/면 추출
    const n = parts[2]
    if (n.endsWith('동') || n.endsWith('읍') || n.endsWith('면') || n.endsWith('리')) {
      neighborhood = n
    }
  }

  return { region, district, neighborhood }
}
