// 시/도 목록
export const getRegions = (): string[] => {
  return Object.keys(REGIONS)
}

// 특정 시/도의 구/군 목록
export const getDistricts = (region: string): string[] => {
  return region && REGIONS[region] ? Object.keys(REGIONS[region]) : []
}

// 특정 구/군의 동 목록
export const getNeighborhoods = (region: string, district: string): string[] => {
  return region && district && REGIONS[region]?.[district] ? REGIONS[region][district] : []
}

// 경기도 시 목록 (구가 있는 대도시와 일반 시/군 분리)
export const getGyeonggiCities = (): string[] => {
  const districts = getDistricts('경기')
  const cities = new Set<string>()

  districts.forEach(d => {
    // "수원시 장안구" 형태에서 "수원시" 추출
    if (d.includes(' ')) {
      cities.add(d.split(' ')[0])
    } else {
      // 일반 시/군 (남양주시, 화성시 등)
      cities.add(d)
    }
  })

  return Array.from(cities)
}

// 경기도 특정 시의 구 목록 (구가 있는 대도시만)
export const getGyeonggiGus = (city: string): string[] => {
  const districts = getDistricts('경기')
  const gus: string[] = []

  districts.forEach(d => {
    // "수원시 장안구" 형태에서 해당 시의 구만 추출
    if (d.startsWith(city + ' ')) {
      const gu = d.split(' ')[1]
      if (gu) gus.push(gu)
    }
  })

  return gus
}

// 경기도 시가 구를 가진 대도시인지 확인
export const isGyeonggiCityWithGu = (city: string): boolean => {
  return getGyeonggiGus(city).length > 0
}

// 주소에서 지역 정보 파싱 (카카오맵 주소 → region, district, neighborhood)
// 법정동 주소가 오면 행정동으로 자동 변환
export const parseAddress = (address: string): { region: string; district: string; neighborhood: string } => {
  const parts = address.split(' ').filter(p => p.trim())

  let region = ''
  let district = ''
  let neighborhood = ''

  if (parts.length >= 1) {
    // 시/도 추출 (서울특별시 → 서울, 경기도 → 경기)
    const r = parts[0]
      .replace('특별시', '')
      .replace('광역시', '')
      .replace('특별자치시', '')
      .replace('특별자치도', '')
      .replace('도', '')
    region = r
  }

  if (parts.length >= 2) {
    // 경기도 대도시 (수원시, 성남시, 고양시, 용인시, 안산시, 안양시)는 4단계 주소
    // 예: 경기도 수원시 장안구 파장동 → district="수원시 장안구", neighborhood="파장동"
    const isGyeonggiCity = region === '경기' && parts.length >= 3 && parts[2].endsWith('구')

    if (isGyeonggiCity) {
      // 경기도 대도시: 시+구를 합쳐서 district로 사용
      district = `${parts[1]} ${parts[2]}`
      if (parts.length >= 4) {
        const n = parts[3]
        if (n.endsWith('동') || n.endsWith('읍') || n.endsWith('면') || n.endsWith('리')) {
          neighborhood = n
        }
      }
    } else {
      // 일반 주소: 기존 방식
      district = parts[1]
      if (parts.length >= 3) {
        const n = parts[2]
        if (n.endsWith('동') || n.endsWith('읍') || n.endsWith('면') || n.endsWith('리')) {
          neighborhood = n
        }
      }
    }
  }

  // 법정동 → 행정동 변환
  if (neighborhood) {
    neighborhood = convertBeopjeongToHaengjeong(region, district, neighborhood)
  }

  return { region, district, neighborhood }
}
